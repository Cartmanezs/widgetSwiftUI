import SwiftUI

extension Font {
    static func sfProDisplayBold(size: CGFloat) -> Font {
        .custom("SF Pro Display Bold", size: size)
    }
    static func sfProRoundedRegular(size: CGFloat) -> Font {
        .custom("SF Pro Rounded Regular", size: size)
    }
    static func courgetteRegular(size: CGFloat) -> Font {
        .custom("Courgette Regular", size: size)
    }
}

